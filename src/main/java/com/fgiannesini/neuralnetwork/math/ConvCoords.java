package com.fgiannesini.neuralnetwork.math;

public class ConvCoords {

    private final int x;
    private final int y;

    public ConvCoords(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
